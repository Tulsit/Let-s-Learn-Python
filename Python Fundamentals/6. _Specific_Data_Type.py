'''
Topic 6 : Setting the Specific Data Type : If you want to specify the data type, you can use the following constructor functions:
Syntax :
        variable_name = data_type(value)
        print(variable_name)
'''
a = int(3)
print(a)
print(type(a))

b = str("Hello!")
print(b)
print(type(b))

c = float(10.5)
print(c)
print(type(c))

d = bool(False)
print(d)
print(type(d))