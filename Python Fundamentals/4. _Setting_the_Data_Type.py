'''
Topic 4 : Setting the Data Type : Set the data type by assigning a value to a variable.
--> Use different variables for printing different values.
--> Basic data types :- str, int, float, bool
Syntax :
       String:-
       variable_name = "value"
       print(variable_name)

       Integer:-
       variable_name = number
       print(variable_name)

       Float:-
       variable_name = number.number
       print(variable_name)

        Boolean:-
       variable_name = True
                OR
       variable_name = False


'''
s = "This is a string."     #str
print(s)

i = 5                       #int
print(i)

f = 1.75                    #float
print(f)

b1 = True                   #bool
print(b1)

b2 = False                  #bool
print(b2)