'''
Topic 5 : Getting the Data Type : You can get the data type of any object by using the type() function:
Syntax :
        variable_name = value
        print(type(variable_name))
'''

a = 3
print(type(a))

b = "Hello!"
print(type(b))

c = 10.5
print(type(c))

d = True
print(type(d))